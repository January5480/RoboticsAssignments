DATAS SEGMENT
    ;此处输入数据段代码 
    STR1 DB 'I AM A STUDENT!$' 
DATAS ENDS

STACKS SEGMENT
    ;此处输入堆栈段代码
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    ;此处输入代码段代码
    MOV SI,OFFSET STR1
    
AGAIN:
	MOV AL,[SI]
	CMP AL,'$'
	JE DONE
	CMP AL,'A'
	JB NEXT
	CMP AL,'Z'
	JA NEXT
	ADD AL,20H
	MOV [SI],AL
NEXT:
	INC SI	
	JMP AGAIN
DONE:
	MOV DX,OFFSET STR1
	MOV AH,9
	INT 21H

    MOV AH,4CH
    INT 21H
CODES ENDS
    END START


